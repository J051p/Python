# Operatori pridruživanja

x = 5
print(x) # x = 5

x +=3
print(x) # x = x + 3

x-=3
print(x) # x = x - 3

x *=3
print(x) # x = x * 3

x /=3
print(x) # x = x / 3

x %=3
print(x) # x = x % 3

x //=3
print(x) # x = x // 3

x **=3
print(x) # x = x ** 3

x &=3
print(x) # x = x & 3

x |= 3
print(x) # x = x | 3

x ^=3 
print(x) # x = x ^ 3

x >>=3
print(x) # x = >> 3

x <<=3
print(x) # x = x << 3



